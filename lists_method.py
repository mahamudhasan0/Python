#List Method
list = [1,7,4,30,23,40]
print(list)
list.sort() #sorts the list
list.reverse() #reverses list
list.append(98) # adds 98 at the end of the list
list.insert(2,67) # insert 67 index no 2
list.pop(2) #remove element from index no 2
list.remove(23) #remove 23 from the list
print(list)